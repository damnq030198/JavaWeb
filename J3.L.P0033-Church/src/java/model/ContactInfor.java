package model;

public class ContactInfor {
    String id, contact, detail;

    public ContactInfor() {
    }

    public ContactInfor(String id, String contact, String detail) {
        this.id = id;
        this.contact = contact;
        this.detail = detail;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }
    
            
}
